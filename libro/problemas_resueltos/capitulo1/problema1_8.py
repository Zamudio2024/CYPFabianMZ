X1 = float(input("Introduzca el valor de X1 de P1:"))
Y1 = float(input("Introduzca el valor de Y1 de P1:"))
X2 = float(input("Introduzca el valor de X2 de P2:"))
Y2 = float(input("Introduzca el valor de Y2 de P2:"))
DIS = ((X1 - X2) ** 2 + (Y1 - Y2) ** 2) ** 0.5
print(f"El valor de la distancia entre P1 y P2 es: {DIS}")
